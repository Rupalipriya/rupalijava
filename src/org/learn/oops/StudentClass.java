package org.learn.oops;

public class StudentClass {
	private  int sid;
	private  String sname;
	private  int  sroll;
	private  float smarks;
	private  char  sgrade;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getSroll() {
		return sroll;
	}
	public void setSroll(int sroll) {
		this.sroll = sroll;
	}
	public float getSmarks() {
		return smarks;
	}
	public void setSmarks(float smarks) {
		this.smarks = smarks;
	}
	public char getSgrade() {
		return sgrade;
	}
	public void setSgrade(char sgrade) {
		this.sgrade = sgrade;
	}
	
	
	

}
