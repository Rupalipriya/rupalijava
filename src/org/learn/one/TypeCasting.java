package org.learn.one;

public class TypeCasting {

	public static void main(String[] args) {
		double d=22.5;
		int i=10;
	float f=23.4f;
		double d1=i;
		System.out.println(f);
		System.out.println(d1);
		int j=(int)d;
		float f1= (float)d;
		System.out.println(f1);
		System.out.println(j);
	}

}
